const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let chunks = expr.match(/.{1,10}/g);
    let message = "";
    for (let chunk of chunks) {
        if (chunk === "**********") {
            message += " ";
        } else {
            let trimmed = chunk.replace(/^0+/, "");
            let morse = trimmed.replace(/10/g, ".").replace(/11/g, "-");
            let letter = MORSE_TABLE[morse];
            message += letter;
        }
    }
    return message;
}

module.exports = {
    decode
}



